package br.com.guedes.apartments.models;

import br.com.guedes.apartments.models.enums.Role;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import java.io.Serializable;
import java.util.Collection;
import java.util.HashSet;
import java.util.List;
import java.util.Set;
import java.util.stream.Collectors;

public class UserRequest implements UserDetails {

    private Long id;
    private String cpf;
    private String name;
    private String password;
    private Role role;
    public UserRequest() {}

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
       if (this.role == Role.ADMIN_SUPREME) {
           return List.of(new SimpleGrantedAuthority("ROLE_ADMIN_SUPREME"), new SimpleGrantedAuthority("ROLE_USER_NOOB"));
       }

       return List.of(new SimpleGrantedAuthority("ROLE_USER_NOOB"));
    }

    public String getPassword() {
        return password;
    }

    @Override
    public String getUsername() {
        return name;
    }

    @Override
    public boolean isAccountNonExpired() {
        return false;
    }

    @Override
    public boolean isAccountNonLocked() {
        return false;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return false;
    }

    @Override
    public boolean isEnabled() {
        return false;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Role getRole() {
        return role;
    }

    public void setRole(Role role) {
        this.role = role;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }
}
